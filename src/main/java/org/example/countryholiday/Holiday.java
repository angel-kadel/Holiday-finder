package org.example.countryholiday;

public class Holiday {
    private String name; // Name of the holiday
    private String date; // Date of the holiday
    private String country; // Country where the holiday is celebrated
    private String description; // Description of the holiday

    public String getName() {
        return name; // Get the name of the holiday
    }

    public void setName(String name) {
        this.name = name; // Set the name of the holiday
    }

    public String getDate() {
        return date; // Get the date of the holiday
    }

    public void setDate(String date) {
        this.date = date; // Set the date of the holiday
    }

    public String getCountry() {
        return country; // Get the country where the holiday is celebrated
    }

    public void setCountry(String country) {
        this.country = country; // Set the country where the holiday is celebrated
    }

    public String getDescription() {
        return description; // Get the description of the holiday
    }

    public void setDescription(String description) {
        this.description = description; // Set the description of the holiday
    }
}
